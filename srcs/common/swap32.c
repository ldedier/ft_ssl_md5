#include "ft_ssl.h"

void	swap_uint32(uint32_t *value)
{
	*value = ((((*value) & 0xff000000) >> 24)
		| (((*value) & 0xff0000) >> 8) | (((*value) & 0xff00) << 8)
				| (((*value) & 0xff) << 24));
}
